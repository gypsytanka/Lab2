#include <stdio.h>
#include <locale.h>
#include <math.h>

int main() {
    
    // Шаг 1: Задание конкретных значений переменных
    double H = 5000.0;    // высота полета самолета (м)
    double Vc = 250.0;    // скорость самолета (м/с)
    double Vp = 300.0;    // скорость ракеты (м/с)

    setlocale(LC_CTYPE, "");

    // Шаг 2: Проверка условия достижимости цели
    if (Vp <= Vc) {
        printf("ОШИБКА: Ракета не может догнать цель!\n");
        printf("Скорость ракеты (%.1f м/с) должна быть больше скорости самолета (%.1f м/с)\n", Vp, Vc);
        return 1;
    }

    // Шаг 3: Вычисление времени полета ракеты до встречи
    double t = H / sqrt(Vp * Vp - Vc * Vc);

    // Шаг 4: Вычисление дальности поражения цели
    double S = Vc * t;

    // Шаг 5: Форматированный вывод результатов
    printf("РАСЧЕТ ДАЛЬНОСТИ ПОРАЖЕНИЯ ЦЕЛИ\n");
    printf("===============================\n\n");

    printf("ИСХОДНЫЕ ДАННЫЕ:\n");
    printf(" Высота полета самолета: %.1f м\n", H);
    printf(" Скорость самолета: %.1f м/с\n", Vc);
    printf(" Скорость ракеты: %.1f м/с\n\n", Vp);

    printf("РАСЧЕТ:\n");
    printf("Время полета ракеты до встречи:\n");
    printf("  t = H / sqrt(Vp*Vp - Vc*Vc) = %.1f / sqrt(%.1f * %.1f - %.1f * %.1f) = %.2f с\n", H, Vp, Vp, Vc, Vc, t);

    printf("Дальность поражения цели:\n");
    printf("  S = Vc * t = %.1f * %.2f = %.2f м\n\n", Vc, t, S);

    printf("ИТОГОВЫЙ ОТВЕТ:\n");
    printf("Дальность поражения цели S = %.2f м (%.2f км)\n", S, S / 1000);

    return 0;
}